package encapsulation;

public class Student {
String name;
int id;
int mark;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getMark() {
	return mark;
}
public void setMark(int mark) {
	this.mark = mark;
}
}
